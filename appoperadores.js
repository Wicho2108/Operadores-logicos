// Operador NOT
let negacion;
    negacion = true;
    console.log(negacion);
    negacion = !true;
    console.log(negacion);

// Operador OR
let combinacionOR;
    combinacionOR = true || false;
    console.log(combinacionOR);
    combinacionOR = true || true;
    console.log(combinacionOR);
    combinacionOR = false || false;
    console.log(combinacionOR);
    combinacionOR = false || true;
    console.log(combinacionOR);

// Operador AND
let combinacionAND;
    combinacionAND = true && true;
    console.log(combinacionAND);
    combinacionAND = true && false;
    console.log(combinacionAND);